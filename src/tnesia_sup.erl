-module(tnesia_sup).

-behaviour(supervisor).

-export([
	 start_link/0,
	 init/1
	]).

%%====================================================================
%% Main API
%%====================================================================

%%--------------------------------------------------------------------
%% start_link
%%--------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%%====================================================================
%% Supervisor Callbacks
%%====================================================================

%%--------------------------------------------------------------------
%% init
%%--------------------------------------------------------------------
init([]) ->
    {ok, {{one_for_one, 5, 10}, 
	  [
	   {tnesia_http_sup, 
	    {tnesia_http_sup, start_link, []},
	    transient,
	    3000,
	    supervisor,
	    [tnesia_sup]}
	  ]}}.

