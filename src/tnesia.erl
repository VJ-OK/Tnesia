-module(tnesia).

-export([
	 start/0
	]).

-include("tnesia.hrl").

%%====================================================================
%% Tnesia Management
%%====================================================================

%%--------------------------------------------------------------------
%% start
%%--------------------------------------------------------------------
start() ->
    application:start(tnesia),
    [tnesia_http_tql:add_listener() || 
	_ <- lists:seq(1, ?CONFIG_HTTP_TQL_LISTENERS)],

    ok.
